#include "libft.h"
#include <assert.h>

int	ft_g_index_of(void *arr, int len, int n)
{
	int	i;

	i = 0;
	while (i < len)
	{
		if (arr[i] == n)
			return (i);
		i++;
	}
	return (-1);
}